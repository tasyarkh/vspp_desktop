    public void cariData(){
        String cari = txtCari.getText();
        tb.setRowCount(0);
        try{
            String sql = "SELECT * FROM transaksi WHERE nama LIKE '%"+cari+"%' OR nisn LIKE '%"+cari+"%'";
            rs = st.executeQuery(sql);
            Object [] data = new Object[10];
            while(rs.next()){
                    data[0] = rs.getString("id");
                    data[1] = rs.getString("nisn");
                    data[2] = rs.getString("nama");
                    data[3] = rs.getString("tgl_bayar");
                    data[4] = rs.getString("bln_bayar");
                    data[5] = rs.getString("th_bayar");
                    data[6] = rs.getString("spp_id");
                    data[7] = rs.getString("jml_bayar");
                    data[8] = rs.getString("ket");
                tb.addRow(data);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Data Tidak Tersedia");
        }
        
        tblTransaksi.setModel(tb);
    }
    
