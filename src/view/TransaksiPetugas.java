   public void tmp(String orders) {
        try{
            tb.getDataVector().removeAllElements();
            tb.fireTableDataChanged();
            st = con.createStatement();
            rs = st.executeQuery("SELECT * FROM transaksi");
            
            while(rs.next()){
                Object[] data = {
                    rs.getString("id"),
                    rs.getString("nama"),
                    rs.getString("nisn"),
                    rs.getString("tgl_bayar"),
                    rs.getString("bln_bayar"),
                    rs.getString("th_bayar"),
                    rs.getString("spp_id"),
                    rs.getString("jml_bayar"),
                    rs.getString("ket"),
                };
                tb.addRow(data);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
   }
   
   public void sppId(){
        try{
            st = con.createStatement();
            rs = st.executeQuery("SELECT * FROM spp");
            
            while(rs.next()){
                spp.addItem(rs.getString("tahun")+"|"+rs.getString("bulan")+"|"+rs.getString("nominal"));
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
   
   public void petugasId(){
        try{
            st = con.createStatement();
            rs = st.executeQuery("SELECT * FROM petugas WHERE status='AKTIF'");
            
            while(rs.next()){
                petugas.addItem(rs.getString("nama_petugas"));
            }   
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }

