    public void cariData(){
        String cari = txtCari.getText();
        tb.setRowCount(0);
        try{
            String sql = "SELECT * FROM petugas WHERE nama_petugas LIKE '%"+cari+"%' OR username LIKE '%"+cari+"%'";
            rs = st.executeQuery(sql);
            Object [] data = new Object[5];
            while(rs.next()){
                data[0] = rs.getString("nama_petugas");
                data[1] = rs.getString("username");
                data[2] = rs.getString("level");
                data[3] = rs.getString("status");
                
                tb.addRow(data);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Data Tidak Tersedia");
        }
        
        tbl.setModel(tb);
    }
